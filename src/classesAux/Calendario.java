	public Calendario(int ano, int mes, int dia, int hora, int minuto) {
		//Inicia com tempo informado
		this.ids = TimeZone.getAvailableIDs(-3 * 60 * 60 * 1000);
		this.timeZone = new SimpleTimeZone(-3 * 60 * 60 * 1000, ids[0]); 
		this.calendar = new GregorianCalendar(timeZone);
		this.date = new Date();
		this.calendar.setTime(date);
		this.calendar.set(ano, mes, dia, hora, minuto, 0);
		
	}
	

